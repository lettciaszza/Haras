class PanelBuscaEq extends JPanel{
    private TableDark table;
    private Equino eqSelecionado;
    private EquinoController eqController= new EquinoController();
    private MyTextField nmEqSelecionado;
    public PanelBuscaEq(){
        
        this.setBackground(new Color(255,255,255));
        
        JPanel panelTitulo = new JPanel();
        panelTitulo.setLayout(new MigLayout("fill","[center]","push[center]push"));
        panelTitulo.setBackground(new Color (204, 83, 18));
        JLabel titulo = new JLabel("Gerenciar Ra√ßas");
        titulo.setFont(new Font("sansserif",3,36));
        titulo.setForeground(Color.white);
        panelTitulo.add(titulo);
      
        this.setLayout( new MigLayout("wrap","[center]","[top]push[]10[]10[]push[]push[]"));
        this.add(panelTitulo,"span,w 100%,h 15%, pad -10 -15 0 5, wrap");
        
        
        JLabel lblSelecionado = new JLabel("Equino Selecionado");
        lblSelecionado.setFont(new Font("sansserif",1,15));
        lblSelecionado.setForeground(new Color(74,37,20));
        nmEqSelecionado = new MyTextField();
        this.add(lblSelecionado,"center,split 2,gapright 20");
        this.add(nmEqSelecionado,"w 30%,center");
        
        table = new TableDark();
        this.atualizaTabelaEquino();
        JScrollPane scrollPane = new JScrollPane(table);
        table.fixTable(scrollPane);
        table.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent evt) {
                if (evt.getClickCount() == 2) {
                    JTable target = (JTable) evt.getSource();
                    int row = target.getSelectedRow();
                    Equino eq =(Equino) table.getModel().getValueAt(row, -1);
                    eqSelecionado = eq;
                    nmEqSelecionado.setText(eqSelecionado.getNome());
                }
            }
        });
        this.add(scrollPane,"skip, w 95%, h 70%,pad 0 -15 10 -2");
    }
    public void atualizaTabelaEquino(){
        table.setModel(eqController.atualizarTabela());
    }
    public Equino getEquinoSelecionado(){
        return this.eqSelecionado;
    }
    
}
